@bot.event
async def on_message(message):
    if message.author.bot:
        return
    
    if message.channel.id == MESSAGE_INPUT_CHANNEL_ID:
        content = message.content.replace('@', 'Ôº†')  # ‡∏õ‡πâ‡∏≠‡∏á‡∏Å‡∏±‡∏ô‡∏Å‡∏≤‡∏£ mention ‡∏à‡∏£‡∏¥‡∏á
        mentions = []
        remaining_words = []

        for word in content.split():
            if word.startswith('Ôº†'):
                username = word[1:]
                member = discord.utils.find(lambda m: m.name == username or m.display_name == username, message.guild.members)
                if member:
                    mentions.append(member.mention)  # ‡πÉ‡∏ä‡πâ mention ‡∏à‡∏£‡∏¥‡∏á‡πÉ‡∏ô ANNOUNCE_CHANNEL_ID
                else:
                    remaining_words.append(word)
            else:
                remaining_words.append(word)

        mention_text = " ".join(mentions)
        final_message = " ".join(remaining_words)

        if mentions:
            final_message = f"{mention_text}\n{final_message}"

        try:
            announce_channel = await bot.fetch_channel(ANNOUNCE_CHANNEL_ID)
            await announce_channel.send(final_message, allowed_mentions=discord.AllowedMentions(users=True, roles=True, everyone=False))
            await message.delete()
            await log_message(f"üì© ‡∏Ç‡πâ‡∏≠‡∏Ñ‡∏ß‡∏≤‡∏°‡∏ñ‡∏π‡∏Å‡∏™‡πà‡∏á‡πÇ‡∏î‡∏¢ {message.author} ({message.author.id}): {final_message}")
        except discord.errors.NotFound:
            error_msg = "‚ùå ‡πÑ‡∏°‡πà‡∏û‡∏ö‡∏ä‡πà‡∏≠‡∏á‡∏õ‡∏£‡∏∞‡∏Å‡∏≤‡∏® ‡∏Å‡∏£‡∏∏‡∏ì‡∏≤‡∏ï‡∏£‡∏ß‡∏à‡∏™‡∏≠‡∏ö ANNOUNCE_CHANNEL_ID"
            print(error_msg)
            await log_message(error_msg)
        except discord.errors.Forbidden:
            error_msg = "‚ùå ‡∏ö‡∏≠‡∏ó‡πÑ‡∏°‡πà‡∏°‡∏µ‡∏™‡∏¥‡∏ó‡∏ò‡∏¥‡πå‡∏™‡πà‡∏á‡∏Ç‡πâ‡∏≠‡∏Ñ‡∏ß‡∏≤‡∏°‡πÑ‡∏õ‡∏¢‡∏±‡∏á‡∏ä‡πà‡∏≠‡∏á‡∏õ‡∏£‡∏∞‡∏Å‡∏≤‡∏®"
            print(error_msg)
            await log_message(error_msg)

    await bot.process_commands(message)

